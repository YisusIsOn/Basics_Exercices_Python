"""
Escribir un programa que pregunte al usuario su edad y muestre por pantalla si es mayor de edad o no.
"""

print("\n")
print("".center(50,"-"))
edad = int(input("Ingrese su edad: "))

if edad >= 18:
    print("Usted es mayor de edad")
else:
    print("Usted es menor de edad")

print("".center(50,"-"))
print("\n")